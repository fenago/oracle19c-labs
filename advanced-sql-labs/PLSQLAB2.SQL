var g_salary number
DECLARE
STAFF_REC      STAFF%ROWTYPE;
TOT_COMP       NUMBER(11,2) := 0;
BONUS          NUMBER(11,2) := 0;
BEGIN
STAFF_REC.ID := &EMP_ID;
SELECT SALARY,NVL(COMM,0) INTO STAFF_REC.SALARY,STAFF_REC.COMM
FROM STAFF WHERE ID = STAFF_REC.ID;
BONUS := STAFF_REC.COMM * .05;
TOT_COMP := BONUS + STAFF_REC.SALARY + STAFF_REC.COMM;
:G_SALARY := TOT_COMP;
END;
/
print g_salary