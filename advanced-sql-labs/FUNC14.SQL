DECLARE FUNCTION SPELL_NUMBER (DEPT_NO in NUMBER)
       RETURN VARCHAR2 IS
       TMP VARCHAR2(30);
BEGIN
     IF  DEPT_NO < 10 THEN
     RETURN NULL;
     ELSE
      SELECT TO_CHAR(TO_DATE(LPAD(TO_CHAR(DEPT_NO),4,'0'),'YYYY'),'YEAR')
     INTO TMP
     FROM DUAL;
     RETURN TMP;
     END IF;
END;
BEGIN
DBMS_OUTPUT.PUT_LINE(SPELL_NUMBER(22));
DBMS_OUTPUT.PUT_LINE(SPELL_NUMBER(15));
DBMS_OUTPUT.PUT_LINE(SPELL_NUMBER(77));
END;
/
